public class RightTriangleOdd
{
    public static void main(String args[])
    {
        int i,j,k=1;
        for(i=0;i<5;i++)
        {
            for(j=0;j<k;j++)
            {
                System.out.println("* ");
            }
            k=k+2;
            System.out.println();
        }
    }
}
